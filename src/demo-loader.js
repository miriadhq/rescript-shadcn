// Static demo component loader using React.lazy.
// Each import is explicit so bundlers can analyze them individually.
// React.lazy ensures components are loaded and rendered client-side,
// avoiding RSC client reference issues with nested submodule access.
import * as React from "react"

function lazyDemo(importFn) {
  return React.lazy(async () => {
    const mod = await importFn()
    const component = mod.make || (mod.default && mod.default.make) || mod.default
    return { default: component }
  })
}

