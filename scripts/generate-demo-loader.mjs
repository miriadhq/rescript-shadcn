#!/usr/bin/env node
// Generates src/demo-loader.js from registry/base/examples/*.res files.
// Usage: node scripts/generate-demo-loader.mjs

import { readdirSync, writeFileSync } from "fs";
import { basename, join } from "path";

const EXAMPLES_DIR = "registry/base/examples";
const OUTPUT_FILE = "src/demo-loader.js";

// Files to exclude (e.g. broken imports like next/font/google)
const EXCLUDE = new Set([]);

const names = readdirSync(EXAMPLES_DIR)
  .filter((f) => f.endsWith(".res") && !f.startsWith("."))
  .map((f) => basename(f, ".res"))
  .filter((name) => !EXCLUDE.has(name))
  .sort();

const entries = names
  .map(
    (name) =>
      `  ${name}: lazyDemo(() => import("../${EXAMPLES_DIR}/${name}.res.mjs")),`
  )
  .join("\n");

const output = `// @generated by scripts/generate-demo-loader.mjs — do not edit manually.
import * as React from "react"

function lazyDemo(importFn) {
  return React.lazy(async () => {
    const mod = await importFn()
    const component = mod.make || (mod.default && mod.default.make) || mod.default
    return { default: component }
  })
}

export const demos = {
${entries}
};

export function getDemo(name) {
  return demos[name] || null;
}
`;

writeFileSync(OUTPUT_FILE, output);
console.log(`Generated ${OUTPUT_FILE} with ${names.length} demos (excluded: ${[...EXCLUDE].join(", ")})`);
